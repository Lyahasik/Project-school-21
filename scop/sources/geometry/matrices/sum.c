#include "matrices.h"

/*
** Сложение матриц.
** @param m1 матрица.
** @param m2 матрица.
** @return сумма матриц.
*/

void	matrix_sum_4x4(t_matrix4 *m1, t_matrix4 m2)
{
	(*m1)[0].x += m2[0].x;
	(*m1)[0].y += m2[0].y;
	(*m1)[0].z += m2[0].z;
	(*m1)[0].w += m2[0].w;
	(*m1)[1].x += m2[1].x;
	(*m1)[1].y += m2[1].y;
	(*m1)[1].z += m2[1].z;
	(*m1)[1].w += m2[1].w;
	(*m1)[2].x += m2[2].x;
	(*m1)[2].y += m2[2].y;
	(*m1)[2].z += m2[2].z;
	(*m1)[2].w += m2[2].w;
	(*m1)[3].x += m2[3].x;
	(*m1)[3].y += m2[3].y;
	(*m1)[3].z += m2[3].z;
	(*m1)[3].w += m2[3].w;
}
